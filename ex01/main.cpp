#include "iter.hpp"


int		main()
{
	int		a[5] = { 0, 1, 2, 3, 4 };
	int		i = 0;

	while (i < 5)
	{
		std::cout << a[i] << ' ';
		i++;	
	}
	std::cout << '\n';
	iter<int>(a, 5, &ft_add);
	i = 0;
	while (i < 5)
	{
		a[i] = i;
		std::cout << a[i] << ' ';
		i++;	
	}
	std::cout << '\n';
}
